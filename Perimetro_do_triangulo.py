# Dados 3 lados de um triângulo, calcule e mostre seu perímetro (soma dos lados).

l1 = float(input("Quantos centímetros o primeiro lado? "))
l2 = float(input("Quantos centímetros o segundo  lado? "))
l3 = float(input("Quantos centímetros o terceiro  lado? "))

perimetro = l1 + l2 + l3

print(f"O perímetro do triângulo mede {perimetro:.2f} ")
