# Dados 3 lados de um triângulo, calcule e mostre seu perímetro (soma dos lados).

l1 = float(input("Quantos centímetros mede este lado? "))
l2 = float(input("Quantos centímetros mede este lado? "))
l3 = float(input("Quantos centímetros mede este lado? "))

perimetro = l1 + l2 + l3

print(f"O perímetro do triângulo mede {perimetro:.2f} centímetros ")